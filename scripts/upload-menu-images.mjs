import { createClient } from '@supabase/supabase-js';
import { promises as fs } from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const ENV_PATH = path.join(ROOT, '.env');
const IMAGE_DIR = path.join(ROOT, 'assets', 'images');
const DEFAULT_BUCKET = 'menu-images';
const IMAGE_EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp']);

const readEnvFile = async () => {
  try {
    const content = await fs.readFile(ENV_PATH, 'utf8');
    return Object.fromEntries(
      content
        .split(/\r?\n/)
        .map((line) => line.trim())
        .filter((line) => line && !line.startsWith('#') && line.includes('='))
        .map((line) => {
          const idx = line.indexOf('=');
          const key = line.slice(0, idx).trim();
          const rawValue = line.slice(idx + 1).trim();
          const value = rawValue.replace(/^['"]|['"]$/g, '');
          return [key, value];
        }),
    );
  } catch {
    return {};
  }
};

const mimeFromExt = (filename) => {
  const ext = path.extname(filename).toLowerCase();
  if (ext === '.png') return 'image/png';
  if (ext === '.webp') return 'image/webp';
  return 'image/jpeg';
};

const stripTrailingSlash = (value = '') => value.replace(/\/+$/, '');

const ensureBucket = async (client, bucket) => {
  const { data, error } = await client.storage.getBucket(bucket);
  if (!error && data) return;

  const { error: createError } = await client.storage.createBucket(bucket, {
    public: true,
    fileSizeLimit: 10 * 1024 * 1024,
    allowedMimeTypes: ['image/jpeg', 'image/png', 'image/webp'],
  });
  if (createError) throw createError;
};

const listImages = async () => {
  const entries = await fs.readdir(IMAGE_DIR, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((filename) => IMAGE_EXTENSIONS.has(path.extname(filename).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, 'vi'));
};

const main = async () => {
  const env = { ...process.env, ...(await readEnvFile()) };
  const supabaseUrl = stripTrailingSlash(env.VITE_SUPABASE_URL || env.SUPABASE_URL || '');
  const serviceRoleKey = env.SUPABASE_SERVICE_ROLE_KEY || '';
  const anonKey = env.VITE_SUPABASE_ANON_KEY || env.SUPABASE_ANON_KEY || '';
  const supabaseKey = serviceRoleKey || anonKey;
  const bucket = env.SUPABASE_STORAGE_BUCKET || DEFAULT_BUCKET;
  const remotePrefix = (env.SUPABASE_MENU_IMAGE_PREFIX || 'menu').replace(/^\/+|\/+$/g, '');

  if (!supabaseUrl || supabaseUrl.includes('your-project-ref')) {
    throw new Error('Missing VITE_SUPABASE_URL in .env');
  }
  if (!supabaseKey || supabaseKey.includes('your-')) {
    throw new Error('Missing SUPABASE_SERVICE_ROLE_KEY or VITE_SUPABASE_ANON_KEY in .env');
  }

  const client = createClient(supabaseUrl, supabaseKey, {
    auth: { persistSession: false, autoRefreshToken: false },
  });

  if (serviceRoleKey) {
    await ensureBucket(client, bucket);
  }

  const images = await listImages();
  if (!images.length) {
    console.log('No images found in assets/images.');
    return;
  }

  const uploaded = [];
  const failed = [];

  for (const filename of images) {
    const localPath = path.join(IMAGE_DIR, filename);
    const storagePath = `${remotePrefix}/${filename}`;
    const bytes = await fs.readFile(localPath);
    const { error } = await client.storage.from(bucket).upload(storagePath, bytes, {
      cacheControl: '31536000',
      contentType: mimeFromExt(filename),
      upsert: true,
    });

    if (error) {
      failed.push({ filename, message: error.message });
      continue;
    }

    const { data } = client.storage.from(bucket).getPublicUrl(storagePath);
    uploaded.push({ filename, publicUrl: data.publicUrl });
  }

  const manifestPath = path.join(ROOT, 'supabase', 'menu-image-urls.generated.sql');
  const sql = [
    '-- Generated by scripts/upload-menu-images.mjs',
    '-- Maps menu item ids to Supabase Storage public image URLs.',
    '',
    ...uploaded.map(({ filename, publicUrl }) => {
      const id = path.basename(filename, path.extname(filename));
      return `update public.menu_items set image_path = '${publicUrl.replaceAll("'", "''")}' where id = '${id.replaceAll("'", "''")}';`;
    }),
    '',
  ].join('\n');
  await fs.writeFile(manifestPath, sql, 'utf8');

  console.log(`Uploaded: ${uploaded.length}/${images.length}`);
  console.log(`Bucket: ${bucket}`);
  console.log(`Prefix: ${remotePrefix}`);
  console.log(`SQL: ${path.relative(ROOT, manifestPath)}`);

  if (failed.length) {
    console.log('Failed uploads:');
    failed.forEach((item) => console.log(`- ${item.filename}: ${item.message}`));
    process.exitCode = 1;
  }
};

main().catch((error) => {
  console.error(error.message || error);
  process.exit(1);
});
